i = 1  # i değişkeni tanımlandı
i = i + 1  # burada i değişkenini 1 artırdık. bu metod standart metod.
print(i)  # yeni i değişkeni değeri 2 olarak yazdırılır
# çıktı:
# 2

# yukarıda yazılan kodda i=i+1 ifadesinin daha kısa yazımı.

i += 1  # kısa yazım
print(i)  # yeni i değişkeni değeri 3 olarak yazılır.
# çıktı:
# 3

i *= 5  # bu işlemin uzun yazımı i = i*5
print(i)
# çıktı:
# 15

i /= 5  # bu işlemin uzun yazımı i = i/5
print(i)
# çıktı:
# 3

i -= 5  # bu işlemin uzun yazımı i = i-5
print(i)
# çıktı:
# -2
