import turtle # burada turtle kütüphanesi programımıza dahil ettik.
turtle.bgcolor("green") # zemin rengini girdik.
turtle.begin_fill() # çizdiğimiz üçgenin içini doldurmaya başla.
turtle.color("white","yellow") # çigi rengi beyaz dolgu rengim sarı olsun istedik
turtle.fd(200) # ileri 200 birim gittik. fd forward kelimesinin kısaltılmış hali.
turtle.left(120) # sola 120 derece ile döndük
turtle.fd(200) # ileri 200 birim gittik.
turtle.left(120) # sola 120 derece döndük.
turtle.fd(200) # ileri 200 birim gittik.
turtle.end_fill() # çizdiğimiz üçgenin içinin doldurulmasını bitir.
