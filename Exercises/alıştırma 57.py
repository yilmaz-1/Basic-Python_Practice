# aşağıdaki program bir web sayfasını açmda kullanılıyor.

import webbrowser as wb # programa webbrowser modülü wb olarak kısaltılmış bir şekilde dahil edildi.

wb.open("www.udemy.com") # programa dahil ettiğimiz webbrowser modülünün open fonskiyonu kullanılarak parantez
# içerisinde yazılan url adresi web sayfası, program çalıştırıldığında açılacak.
wb.open_new("www.google.com")  # burada ise yeni bir sayafada aç denek.
wb.open_new_tab("www.hotmail.com") # burada ise yeni tabda sekmede aç demek.



